                <p>
                    After studying respectively design and political science (Sciences Po), Fred Raillard and Farid Mokart started as strategic planner and account manager in Paris. They decided to team up and moved to the creative side, working consecutively for 6 major agencies in Paris. They were the two youngest creative directors in France, leading the international accounts of CLM/BBDO Paris. Their first digital hit was for start-up Aucland.com, the French eBay, inviting TV viewers to discover the end of the spot on the Internet. They soon became worldwide creative directors of Pepsi International (ROW). In 2000, Fred & Farid created the highly viral and controversial musical video of British Pop Star Robbie Williams “Rock DJ” which won the Brit Awards, and an MTV award in the USA.
                    <br/><br/>
                    Shots Magazine (Shots 61) chose the French team to start their gallery of the 12 best creative teams in the world (November 2000).
                    <br/><br/>
                    Then they embarked on an international career. At that time, Fred & Farid were the first major French creative team to move abroad and work outside France. In 2001, advertising legend Sir John Hegarty hired them at the prestigious BBH London to lead the biggest Microsoft launch: XBOX (500 million dollars).
                </p>

