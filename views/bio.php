                    In 2007, a new big chapter started, when French financial tycoon Vincent Bolloré offered to finance their own group, Fred & Farid Group. “Fred & Farid are so talented that it was a too good opportunity to pass up” Vincent Bolloré / Campaign (13 Oct 2006).</p>

