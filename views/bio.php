                    <br /><br />
                    In 2012, Fred & Farid Group decided to develop outside Europe and chose to go East. They opened a new digital agency in a 1000m² warehouse in Shanghai. This new agency is 100% social media orientated. In just one year, their Chinese office has worked for 27 brands in China, including Porsche, Coca-Cola, Shang Xia, Sunning, Sogou, Avène… and renowned groups like L’Oréal and Pernod Ricard.
                </p>

